<?php

namespace App\Mail;

use Illuminate\Bus\Queueable;
use Illuminate\Contracts\Queue\ShouldQueue;
use Illuminate\Mail\Mailable;
use Illuminate\Queue\SerializesModels;

class DonorMail extends Mailable
{
    use Queueable, SerializesModels;
    
    /**
     * Create a new message instance.
     *
     * @return void
     */
    public function __construct()
    {
        //
    }

    /**
     * Build the message.
     *
     * @return $this
     */
    public function build()
    {
        return $this->subject('Sparkpost testing mail')
            ->view('mails/donor_mail')
            ->attach(storage_path('app/attachments/Certificate of donation.pdf'))
            ->attach(storage_path('app/attachments/Tax receipt.pdf'));
    }
}
